	<h3>Blood Condition</h3>
    <p>Hemorrhage (bleeding): Blood leaking out of blood vessels may be obvious, as from a wound penetrating the skin. Internal bleeding (such as into the intestines, or after a car accident) may not be immediately apparent.</p>
	<p>Hematoma: A collection of blood inside the body tissues. Internal bleeding often causes a hematoma.</p>
    <p>Leukemia: A form of blood cancer, in which white blood cells multiply abnormally and circulate through the blood. The abnormal white blood cells make getting sick from infections easier than normal.</p>
    <p>Multiple myeloma: A form of blood cancer of plasma cells similar to leukemia. Anemia, kidney failure and high blood calcium levels are common in multiple myeloma.</p>
    <p>Lymphoma: A form of blood cancer, in which white blood cells multiply abnormally inside lymph nodes and other tissues. The enlarging tissues, and disruption of blood's functions, can eventually cause organ failure.</p>
    <p>Anemia: An abnormally low number of red blood cells in the blood. Fatigue and breathlessness can result, although anemia often causes no noticeable symptoms.
    <p>Hemolytic anemia: Anemia caused by rapid bursting of large numbers of red blood cells (hemolysis). An immune system malfunction is one cause.
    <p>Hemochromatosis: A disorder causing excessive levels of iron in the blood. The iron deposits in the liver, pancreas and other organs, causing liver problems and diabetes.
    <p>Sickle cell disease: A genetic condition in which red blood cells periodically lose their proper shape (appearing like sickles, rather than discs). The deformed blood cells deposit in tissues, causing pain and organ damage.
    <p>Bacteremia: Bacterial infection of the blood. Blood infections are serious, and often require hospitalization and continuous antibiotic infusion into the veins.
    <p>Malaria: Infection of red blood cells by Plasmodium, a parasite transmitted by mosquitos. Malaria causes episodic fevers, chills, and potentially organ damage.
    <p>Thrombocytopenia: Abnormally low numbers of platelets in the blood. Severe thrombocytopenia may lead to bleeding.
    <p>Leukopenia: Abnormally low numbers of white blood cells in the blood. Leukopenia can result in difficulty fighting infections.
    <p>Disseminated intravascular coagulation (DIC): An uncontrolled process of simultaneous bleeding and clotting in very small blood vessels. DIC usually results from severe infections or cancer.
    <p>Hemophilia: An inherited (genetic) deficiency of certain blood clotting proteins. Frequent or uncontrolled bleeding can result from hemophilia.
    <p>Hypercoaguable state: Numerous conditions can result in the blood being prone to clotting. A heart attack, stroke, or blood clots in the legs or lungs can result.
    <p>Polycythemia: Abnormally high numbers of red blood cells in the blood. Polycythemia can result from low blood oxygen levels, or may occur as a cancer-like condition.
    <p>Deep venous thrombosis (DVT): A blood clot in a deep vein, usually in the leg. DVTs are dangerous because they may become dislodged and travel to the lungs, causing a pulmonary embolism (PE).
    <p>Myocardial infarction (MI): Commonly called a heart attack, a myocardial infarction occurs when a sudden blood clot develops in one of the coronary arteries, which supply blood to the heart.
