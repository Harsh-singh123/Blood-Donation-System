        <h3>Blood Test</h3>
   <p>Complete blood count: An analysis of the concentration of red blood cells, white blood cells, and platelets in the blood. Automated cell counters perform this test.
   <p>Blood smear: Drops of blood are smeared across a microscope slide, to be examined by an expert in a lab. Leukemia, anemia, malaria, and numerous other blood conditions can be identified with a blood smear.
   <p>Blood type: A test for compatibility before receiving a blood transfusion. The major blood types (A, B, AB, and O) are determined by the protein markers (antigens) present on the surface of red blood cells.
   <p>Coombs test: A blood test looking for antibodies that could bind to and destroy red blood cells. Pregnant women and people with anemia may undergo Coombs testing.
   <p>Blood culture: A blood test looking for infection present in the bloodstream. If bacteria or other organisms are present, they may multiply in the tested blood, allowing their identification.
   <p>Mixing study: A blood test to identify the reason for blood being "too thin" (abnormally resistant to clotting). The patient's blood is mixed in a tube with normal blood, and the mixed blood's properties may provide a diagnosis.
   <p>Bone marrow biopsy: A thick needle is inserted into a large bone (usually in the hip), and bone marrow is drawn out for tests. Bone marrow biopsy can identify blood conditions that simple blood tests cannot. 
